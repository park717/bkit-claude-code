#!/usr/bin/env node
/**
 * Design Post Hook — Suggests scenario matrix generation after PDCA Design completion
 * @version 3.0.0
 *
 * Triggered by PostToolUse hook when Design document is created/updated.
 * Checks if scenarios exist for the feature, suggests creation if missing.
 */
const fs = require('fs');
const path = require('path');

function main() {
  let input;
  try {
    input = JSON.parse(fs.readFileSync('/dev/stdin', 'utf8'));
  } catch {
    process.stdout.write(JSON.stringify({ result: 'allow' }));
    return;
  }

  const toolName = input.tool_name || input.toolName || '';
  const output = input.tool_output || input.output || '';

  // Only trigger on Write/Edit to design documents
  if (!['Write', 'Edit'].includes(toolName)) {
    process.stdout.write(JSON.stringify({ result: 'allow' }));
    return;
  }

  // Check if the output path is a design document
  const filePath = input.tool_input?.file_path || '';
  if (!filePath.includes('design') || !filePath.endsWith('.md')) {
    process.stdout.write(JSON.stringify({ result: 'allow' }));
    return;
  }

  // Extract feature name from path
  const match = filePath.match(/([^/]+)\.design\.md$/);
  if (!match) {
    process.stdout.write(JSON.stringify({ result: 'allow' }));
    return;
  }

  const feature = match[1];
  const scenariosDir = path.join(process.cwd(), 'docs', '02-design', 'scenarios');

  // Check if scenarios already exist
  let hasScenarios = false;
  if (fs.existsSync(scenariosDir)) {
    const files = fs.readdirSync(scenariosDir);
    hasScenarios = files.some(f => f.includes(feature));
  }

  if (!hasScenarios) {
    // Auto-generate skeleton scenario YAML
    if (!fs.existsSync(scenariosDir)) {
      fs.mkdirSync(scenariosDir, { recursive: true });
    }

    const skeletonPath = path.join(scenariosDir, `${feature}.scenarios.yaml`);
    const skeleton = [
      `# Scenario Matrix — Auto-generated by bkit Design Hook`,
      `# Review and fill in specific input/output/constraints`,
      `version: "1.0"`,
      `module: "${feature}"`,
      `file: ""  # Target source file path`,
      `scenarios:`,
      `  - id: "S001"`,
      `    name: "Happy path"`,
      `    description: "Basic success scenario"`,
      `    input: {}`,
      `    expected:`,
      `      status: 200`,
      `    tags: ["happy-path"]`,
      ``,
      `  - id: "S002"`,
      `    name: "Error case"`,
      `    description: "Expected error handling"`,
      `    input: {}`,
      `    expected:`,
      `      status: 400`,
      `    why: ""  # Why this behavior exists`,
      `    constraint: ""  # What must NOT change`,
      `    tags: ["error"]`,
    ].join('\n');

    fs.writeFileSync(skeletonPath, skeleton);

    const message = [
      '',
      `📚 Living Context: Scenario Matrix 스켈레톤이 생성되었습니다.`,
      `   Path: ${skeletonPath}`,
      `   시나리오를 검토하고 구체적인 입력/출력/제약조건을 채워주세요.`,
      '',
    ].join('\n');

    process.stderr.write(message);
  }

  process.stdout.write(JSON.stringify({ result: 'allow' }));
}

main();
